from pydantic import BaseModel


class IdResponse(BaseModel):
    """
    Базовая модель ответа, содержащая только идентификатор.

    Используется для возврата ID объекта при операциях создания, удаления
    или обновления, когда требуется минимальный ответ от сервера.
    """

    id: int  # Уникальный идентификатор объекта
